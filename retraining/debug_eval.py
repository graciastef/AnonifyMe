"""
Standalone eval debug script. Runs against a sample of WiderFace val and the
fc_val label file generated by the pipeline, printing per-image diagnostics.

All paths default to locations used by the pipeline, so you can run with no
arguments if the repo layout is intact:

    python retraining/debug_eval.py

Override individual paths as needed:

    python retraining/debug_eval.py \\
        --onnx retraining_data/<version>/eval/det_10g_<version>.onnx \\
        --fc-val-label retraining_data/<version>/processed/fc_val_label.txt
"""
import argparse
import importlib.util
import random
import sys
from pathlib import Path
from typing import Optional

import cv2
import numpy as np
import torch
from torchvision.ops import box_iou

_HERE      = Path(__file__).resolve().parent          # retraining/
_REPO_ROOT = _HERE.parent                             # repo root


# ── defaults ──────────────────────────────────────────────────────────────────

def _default_onnx() -> str:
    # Prefer the most recently built pipeline ONNX; fall back to buffalo_l.
    candidates = sorted(
        (_REPO_ROOT / "retraining_data").glob("*/eval/det_10g_*.onnx"),
        key=lambda p: p.stat().st_mtime,
        reverse=True,
    ) if (_REPO_ROOT / "retraining_data").exists() else []
    if candidates:
        return str(candidates[0])
    buffalo = Path.home() / ".insightface" / "models" / "buffalo_l" / "det_10g.onnx"
    return str(buffalo)



# ── azure helpers ─────────────────────────────────────────────────────────────

def _azure_container():
    import os
    from dotenv import load_dotenv
    from azure.identity import DefaultAzureCredential
    from azure.storage.blob import BlobServiceClient

    load_dotenv()
    svc = BlobServiceClient(
        account_url=os.environ["AZURE_BLOB_URL"],
        credential=DefaultAzureCredential(),
    )
    return svc.get_container_client(os.environ["AZURE_CONTAINER_NAME"])


def _fetch_latest_fc_val_label(local_dir) -> Optional[str]:
    """Download the most recently uploaded fc_val_label.txt from Azure and return its local path."""
    Path(local_dir).mkdir(parents=True, exist_ok=True)
    try:
        container = _azure_container()
        blobs = [
            b for b in container.list_blobs(name_starts_with="training/datasets/")
            if b.name.endswith("/fc_val_label.txt")
        ]
        if not blobs:
            print("  [WARN] No fc_val_label.txt found in Azure training/datasets/")
            return None
        latest = max(blobs, key=lambda b: b.last_modified)
        print(f"  Latest Azure label : {latest.name}  ({latest.last_modified})")
        local_path = Path(local_dir) / "fc_val_label.txt"
        local_path.write_bytes(
            container.get_blob_client(latest.name).download_blob().readall()
        )
        return str(local_path)
    except Exception as exc:
        print(f"  [WARN] Could not fetch fc_val_label.txt from Azure: {exc}")
        return None


def _pull_fc_images(fc_items, local_dir):
    """Download any fc_val images not yet cached in local_dir from Azure."""
    Path(local_dir).mkdir(parents=True, exist_ok=True)
    try:
        container = _azure_container()
    except Exception as exc:
        print(f"  [WARN] Azure connection failed, skipping image pull: {exc}")
        return

    blob_prefix = "training/failure_cases/images"
    n_downloaded = n_cached = n_failed = 0
    for item in fc_items:
        local_path = Path(item["image"])
        if local_path.exists():
            n_cached += 1
            continue
        blob_name = f"{blob_prefix}/{local_path.name}"
        try:
            data = container.get_blob_client(blob_name).download_blob().readall()
            local_path.write_bytes(data)
            n_downloaded += 1
        except Exception as exc:
            print(f"  [WARN] {blob_name}: {exc}")
            n_failed += 1

    print(f"  {n_downloaded} downloaded, {n_cached} already cached"
          + (f", {n_failed} failed" if n_failed else ""))


def _remap_fc_paths(fc_items, local_dir):
    """Replace absolute pipeline paths in fc_items with paths inside local_dir."""
    Path(local_dir).mkdir(parents=True, exist_ok=True)
    for item in fc_items:
        item["image"] = str(Path(local_dir) / Path(item["image"]).name)


# ── model ─────────────────────────────────────────────────────────────────────

def load_scrfd(onnx_path: str, insightface_dir: str):
    scrfd_py = Path(insightface_dir) / "detection/scrfd/tools/scrfd.py"
    if not scrfd_py.exists():
        sys.exit(f"scrfd.py not found at {scrfd_py}")
    spec = importlib.util.spec_from_file_location("scrfd_onnx_debug", scrfd_py)
    mod  = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    det = mod.SCRFD(model_file=onnx_path)
    det.prepare(-1, input_size=(640, 640))
    return det


# ── labels ────────────────────────────────────────────────────────────────────

def load_labelv2(label_path: str, img_prefix: Optional[str] = None):
    items   = []
    current = None
    missing = 0
    with open(label_path) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            if line.startswith("#"):
                parts    = line[1:].strip().split()
                img_name = parts[0]
                if img_prefix and not Path(img_name).is_absolute():
                    img_path = str(Path(img_prefix) / img_name)
                else:
                    img_path = img_name
                current = {"image": img_path, "bboxes": []}
                items.append(current)
            elif current is not None:
                vals = line.split()[:4]
                x1, y1, x2, y2 = map(float, vals)
                if x2 > x1 and y2 > y1:
                    current["bboxes"].append([x1, y1, x2, y2])

    for item in items:
        item["bboxes"] = np.array(item["bboxes"], dtype=np.float32).reshape(-1, 4)

    for item in items:
        if not Path(item["image"]).exists():
            missing += 1

    if missing:
        print(f"  [WARN] {missing}/{len(items)} image paths not found on disk "
              f"(SageMaker paths? Run this on the same machine as the pipeline)")

    return items


# ── metrics ───────────────────────────────────────────────────────────────────

def wider_face_ap(preds_list, targets_list, iou_thresh=0.5):
    """
    WiderFace-style AP: pool all predictions globally sorted by confidence,
    greedily match to per-image GT boxes, then compute AP via VOC
    area-under-curve interpolation on the resulting global PR curve.
    """
    all_entries = []
    for img_idx, pred in enumerate(preds_list):
        boxes  = pred["boxes"].numpy()
        scores = pred["scores"].numpy()
        for box, score in zip(boxes, scores):
            all_entries.append((float(score), img_idx, box))

    total_gt = sum(t["boxes"].shape[0] for t in targets_list)
    if total_gt == 0 or not all_entries:
        return 0.0

    all_entries.sort(key=lambda x: -x[0])

    gt_matched  = [np.zeros(t["boxes"].shape[0], dtype=bool) for t in targets_list]
    gt_boxes_np = [t["boxes"].numpy() for t in targets_list]

    tp_arr = []
    for _, img_idx, box in all_entries:
        gt_boxes = gt_boxes_np[img_idx]
        matched  = gt_matched[img_idx]
        tp = 0
        if len(gt_boxes):
            b     = box[None]
            ix1   = np.maximum(b[:, 0], gt_boxes[:, 0])
            iy1   = np.maximum(b[:, 1], gt_boxes[:, 1])
            ix2   = np.minimum(b[:, 2], gt_boxes[:, 2])
            iy2   = np.minimum(b[:, 3], gt_boxes[:, 3])
            inter = np.maximum(0, ix2 - ix1) * np.maximum(0, iy2 - iy1)
            area_b  = (b[:, 2] - b[:, 0]) * (b[:, 3] - b[:, 1])
            area_gt = (gt_boxes[:, 2] - gt_boxes[:, 0]) * (gt_boxes[:, 3] - gt_boxes[:, 1])
            iou  = inter / np.maximum(area_b + area_gt - inter, 1e-6)
            best = int(np.argmax(iou))
            if iou[best] >= iou_thresh and not matched[best]:
                matched[best] = True
                tp = 1
        tp_arr.append(tp)

    tp_arr = np.array(tp_arr, dtype=np.float32)
    cum_tp = np.cumsum(tp_arr)
    cum_fp = np.cumsum(1 - tp_arr)
    prec   = cum_tp / (cum_tp + cum_fp)
    rec    = cum_tp / total_gt

    mrec = np.concatenate(([0.], rec, [1.]))
    mpre = np.concatenate(([0.], prec, [0.]))
    for i in range(mpre.size - 1, 0, -1):
        mpre[i - 1] = np.maximum(mpre[i - 1], mpre[i])
    idx = np.where(mrec[1:] != mrec[:-1])[0]
    return float(np.sum((mrec[idx + 1] - mrec[idx]) * mpre[idx + 1]))


def compute_recall_50(preds, targets):
    total_gt = sum(t["boxes"].shape[0] for t in targets)
    if total_gt == 0:
        return 0.0
    matched = 0
    for pred, target in zip(preds, targets):
        if target["boxes"].numel() == 0 or pred["boxes"].numel() == 0:
            continue
        order = torch.argsort(pred["scores"], descending=True)
        ious  = box_iou(pred["boxes"][order], target["boxes"])
        found = torch.zeros(target["boxes"].shape[0], dtype=torch.bool)
        for i in range(ious.shape[0]):
            best_iou, best_gt = ious[i].max(0)
            if best_iou >= 0.5 and not found[best_gt]:
                found[best_gt] = True
        matched += int(found.sum())
    return matched / total_gt


# ── evaluation ────────────────────────────────────────────────────────────────

def run_eval(detector, items, label: str, thresh: float, verbose: bool = False):
    preds_list   = []
    targets_list = []
    pred_counts  = []
    skipped      = 0

    for item in items:
        img = cv2.imread(item["image"])
        if img is None:
            skipped += 1
            continue

        h, w = img.shape[:2]
        dets, _ = detector.detect(img, thresh=thresh, input_size=(640, 640))
        if dets is None or len(dets) == 0:
            dets = np.zeros((0, 5), dtype=np.float32)

        pred_counts.append(len(dets))
        gt_arr = item["bboxes"]

        boxes  = torch.as_tensor(dets[:, :4], dtype=torch.float32)
        scores = torch.as_tensor(dets[:,  4], dtype=torch.float32)
        gt_boxes = torch.as_tensor(gt_arr, dtype=torch.float32)

        preds_list.append({
            "boxes":  boxes,
            "scores": scores,
            "labels": torch.zeros(len(boxes), dtype=torch.long),
        })
        targets_list.append({
            "boxes":  gt_boxes,
            "labels": torch.zeros(len(gt_boxes), dtype=torch.long),
        })

        if verbose:
            print(f"\n  [{Path(item['image']).name}]  image={w}x{h}"
                  f"  gt={len(gt_arr)}  preds={len(dets)}")
            for i, gt in enumerate(gt_arr):
                gx1, gy1, gx2, gy2 = gt
                print(f"    GT #{i}: [{gx1:.0f} {gy1:.0f} {gx2:.0f} {gy2:.0f}]"
                      f"  size={gx2-gx1:.0f}x{gy2-gy1:.0f}px")
                if len(dets):
                    gt_t  = torch.tensor([[gx1, gy1, gx2, gy2]])
                    ious  = box_iou(boxes, gt_t).squeeze(1)
                    best_iou, best_idx = ious.max(0)
                    bd = dets[best_idx]
                    print(f"    Best match: [{bd[0]:.0f} {bd[1]:.0f} {bd[2]:.0f} {bd[3]:.0f}]"
                          f"  conf={bd[4]:.3f}  IoU={best_iou:.3f}"
                          f"  {'✓ match' if best_iou >= 0.5 else '✗ miss (IoU<0.5)'}")
            if len(dets):
                print(f"    Top-5 preds by confidence:")
                for d in dets[:5]:
                    print(f"      [{d[0]:.0f} {d[1]:.0f} {d[2]:.0f} {d[3]:.0f}]  conf={d[4]:.3f}")

    if skipped:
        print(f"  [WARN] Skipped {skipped} images (unreadable)")

    if not preds_list:
        print(f"  [ERROR] No images could be evaluated for {label}")
        return {}

    total_gt = sum(t["boxes"].shape[0] for t in targets_list)
    if total_gt == 0:
        print(f"  [WARN] {label}: label file has image headers but zero valid GT bboxes")
        return {"map_50": 0.0, "recall_50": 0.0}

    ap_50     = wider_face_ap(preds_list, targets_list)
    recall_50 = compute_recall_50(preds_list, targets_list)

    print(f"\n{'='*55}")
    print(f"  {label}")
    print(f"  images evaluated : {len(preds_list)}")
    print(f"  total GT faces   : {total_gt}")
    print(f"  avg preds/image  : {np.mean(pred_counts):.1f}  (thresh={thresh})")
    print(f"  ap@0.5 (wf-style): {ap_50:.4f}  ({ap_50*100:.2f}%)")
    print(f"  recall@0.5       : {recall_50:.4f}  ({recall_50*100:.2f}%)")
    print(f"{'='*55}")

    return {"map_50": ap_50, "recall_50": recall_50}


# ── main ──────────────────────────────────────────────────────────────────────

def main():
    ap = argparse.ArgumentParser(formatter_class=argparse.ArgumentDefaultsHelpFormatter)
    ap.add_argument("--onnx",
                    default=_default_onnx(),
                    help="SCRFD ONNX model path")
    ap.add_argument("--widerface-label",
                    default=str(_HERE / "dataset" / "scrfd_label" / "val" / "labelv2.txt"),
                    help="scrfd_label/val/labelv2.txt")
    ap.add_argument("--widerface-dir",
                    default=str(_HERE / "dataset" / "WIDER_val" / "images"),
                    help="WIDER_val/images/")
    ap.add_argument("--fc-val-label",
                    default=None,
                    help="fc_val_label.txt path; if omitted, latest is pulled from Azure")
    ap.add_argument("--insightface-dir",
                    default=str(_REPO_ROOT / "insightface_repo"),
                    help="insightface_repo root")
    ap.add_argument("--n-widerface", type=int, default=30,
                    help="Number of WiderFace val images to sample")
    ap.add_argument("--thresh",      type=float, default=0.02,
                    help="Detection threshold (try 0.3 or 0.5 to see FP impact)")
    ap.add_argument("--seed",        type=int, default=42)
    ap.add_argument("--fc-images-dir",
                    default=str(_HERE / "dataset" / "fc_val_images"),
                    help="Local directory for caching FC val images downloaded from Azure")
    ap.add_argument("--out",         default=None,
                    help="Write all output to this .txt file (in addition to stdout). "
                         "Defaults to debug_eval_<onnx_stem>.txt next to this script.")
    args = ap.parse_args()

    out_path = Path(args.out) if args.out else _HERE / f"debug_eval_{Path(args.onnx).stem}.txt"

    class _Tee:
        def __init__(self, *streams):
            self._streams = streams
        def write(self, data):
            for s in self._streams:
                s.write(data)
        def flush(self):
            for s in self._streams:
                s.flush()

    _log_file = open(out_path, "w")
    sys.stdout = _Tee(sys.__stdout__, _log_file)

    print(f"ONNX model      : {args.onnx}")
    print(f"WiderFace label : {args.widerface_label}")
    print(f"WiderFace dir   : {args.widerface_dir}")
    print(f"FC val label    : {args.fc_val_label or '(none)'}")
    print(f"InsightFace dir : {args.insightface_dir}")

    if not Path(args.onnx).exists():
        sys.exit(f"\n[ERROR] ONNX not found: {args.onnx}\n"
                 f"Pass --onnx <path> explicitly.")

    print(f"\nLoading model ...")
    detector = load_scrfd(args.onnx, args.insightface_dir)
    print("Model loaded OK")

    # WiderFace val sample
    print(f"\nLoading WiderFace val labels ...")
    wf_all    = load_labelv2(args.widerface_label, img_prefix=args.widerface_dir)
    random.seed(args.seed)
    wf_sample = random.sample(wf_all, min(args.n_widerface, len(wf_all)))
    print(f"  Sampled {len(wf_sample)} / {len(wf_all)} images")
    run_eval(detector, wf_sample,
             label=f"WiderFace val  (n={len(wf_sample)}, thresh={args.thresh})",
             thresh=args.thresh)
    run_eval(detector, wf_sample,
             label=f"WiderFace val  (n={len(wf_sample)}, thresh=0.5 for comparison)",
             thresh=0.5)

    # FC val — always pull latest from Azure
    fc_label = args.fc_val_label
    if not fc_label or not Path(fc_label).exists():
        print(f"\nFetching latest fc_val_label.txt from Azure ...")
        fc_label = _fetch_latest_fc_val_label(args.fc_images_dir)

    if fc_label and Path(fc_label).exists():
        print(f"\nLoading fc_val labels from {fc_label} ...")
        fc_items = load_labelv2(fc_label)
        print(f"  {len(fc_items)} images, "
              f"{sum(len(i['bboxes']) for i in fc_items)} GT faces")

        if fc_items:
            _remap_fc_paths(fc_items, args.fc_images_dir)
            print(f"  Pulling FC images from Azure → {args.fc_images_dir} ...")
            _pull_fc_images(fc_items, args.fc_images_dir)

            print("\n--- Per-image fc_val diagnostics ---")
            run_eval(detector, fc_items,
                     label="FC val  (thresh=0.5)",
                     thresh=0.5, verbose=True)
        else:
            print("  [INFO] fc_val label is empty — no FC val images in latest pipeline run.")
    else:
        print("\n[INFO] No fc_val label available — skipping FC val eval.")

    sys.stdout = sys.__stdout__
    _log_file.close()
    print(f"\n[INFO] Output saved to {out_path}")


if __name__ == "__main__":
    main()
